package com.tnsif.Streamapiprograms;
// to demostrate the stream api
public class Product {

	int id;
	String name;
	float price;     // data members
	
	// parameterized constructor
	public Product(int id, String name, float price) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
	}
}
